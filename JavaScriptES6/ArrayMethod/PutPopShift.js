// push , pop , shift , unshift

// push

const data = [10, 20, 30];
//UnPushed
console.log(data);

data.push(500, 100);
//Pushed
console.log(data);

// pop

console.log(data.pop());
console.log(data);

// shift
console.log(data.shift());
console.log(data);

// unshift
data.unshift(999);
console.log(data);
